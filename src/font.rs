//! Font rendering (wor16.asc/wor16.fon CJK glyphs + ported iso_font ASCII
//! glyphs). Port of `PAL_LoadEmbeddedFont`, `PAL_LoadISOFont` and
//! `PAL_DrawCharOnSurface` in SDLPAL font.c, and the shadow behaviour of
//! `PAL_DrawTextUnescape` in text.c.
//!
//! Design decision for this port: unlike modern SDLPAL (which converts text
//! to Unicode and draws from an embedded Unifont-derived glyph table), this
//! port stays in the game's native byte encoding end-to-end. The DOS data
//! set's `WOR16.ASC` / `WOR16.FON` are encoded in Big5: `WOR16.ASC` is a
//! table of 2-byte big-endian Big5 codes, and `WOR16.FON` holds one 15-row
//! glyph per entry in that table, in the same order. Text bytes are
//! interpreted directly as Big5 (2-byte chars for bytes >= 0x80, followed by
//! a continuation byte) or ASCII (bytes < 0x80), matching how the original
//! DOS engine walked the byte stream.
#![allow(dead_code)] // used incrementally as engine bring-up proceeds

