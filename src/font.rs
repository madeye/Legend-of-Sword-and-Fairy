/// A parsed glyph token from the byte stream.
enum Glyph {
    /// A Big5 double-byte character (2 source bytes consumed).
    Cjk(u16),
    /// A single ASCII/control byte (1 source byte consumed).
    Ascii(u8),
}

pub struct Font {
    /// Big5 code -> index into `wor16.fon`'s glyph table. Built from
    /// wor16.asc, which lists one 2-byte big-endian Big5 code per glyph, in
    /// the same order as the glyphs stored in wor16.fon.
    big5_index: HashMap<u16, usize>,
    /// Raw wor16.fon bytes; glyph `i` is the 30-byte run at
    /// `FON_GLYPH_BASE + i * FON_GLYPH_SIZE`.
    fon_data: Vec<u8>,
}

impl Font {
    /// Load wor16.asc / wor16.fon from the data dir.
    pub fn load(dir: &DataDir) -> io::Result<Font> {
        let asc = dir.read_file("WOR16.ASC")?;
        let fon_data = dir.read_file("WOR16.FON")?;

        // wor16.asc: a table of 2-byte big-endian Big5 codes, one per glyph.
        let n_chars = asc.len() / 2;
        let mut big5_index = HashMap::with_capacity(n_chars);
        for i in 0..n_chars {
            let code = (asc[i * 2] as u16) << 8 | asc[i * 2 + 1] as u16;
            // PAL_LoadEmbeddedFont reads glyph i sequentially from wor16.fon
            // and stores it at unicode_font[code]; later entries for a
            // repeated code overwrite earlier ones, so last-index-wins here.
            big5_index.insert(code, i);
        }

        Ok(Font {
            big5_index,
            fon_data,
        })
    }

    /// Draw raw game-encoded text at (x, y) with the given palette index.
    /// Returns the x coordinate just past the drawn text.
    pub fn draw_text(
        &self,
        surf: &mut Surface,
        text: &[u8],
        x: i32,
        y: i32,
        color: u8,
        shadow: bool,
    ) -> i32 {
        let mut cx = x;
        let mut i = 0usize;
        while i < text.len() {
            let (glyph, advance) = if text[i] >= 0x80 && i + 1 < text.len() {
                (Glyph::Cjk((text[i] as u16) << 8 | text[i + 1] as u16), 16)
            } else {
                (Glyph::Ascii(text[i]), 8)
            };

            if shadow {
                // PAL_DrawTextUnescape's triple-shadow behaviour: draw the
                // glyph in color 0 at (x+1,y), (x,y+1) and (x+1,y+1) before
                // the real, colored glyph at (x,y).
                self.draw_glyph(surf, &glyph, cx + 1, y, 0);
                self.draw_glyph(surf, &glyph, cx, y + 1, 0);
                self.draw_glyph(surf, &glyph, cx + 1, y + 1, 0);
            }
            self.draw_glyph(surf, &glyph, cx, y, color);

            cx += advance;
            i += (advance / 8) as usize;
        }
        cx
    }

    /// Pixel width of a text string as it would be drawn.
    pub fn text_width(&self, text: &[u8]) -> i32 {
        let mut w = 0;
        let mut i = 0usize;
        while i < text.len() {
            if text[i] >= 0x80 && i + 1 < text.len() {
                w += 16;
                i += 2;
            } else {
                w += 8;
                i += 1;
            }
        }
        w
    }

    fn draw_glyph(&self, surf: &mut Surface, glyph: &Glyph, x: i32, y: i32, color: u8) {
        match *glyph {
            Glyph::Cjk(code) => self.draw_cjk_glyph(surf, x, y, code, color),
            Glyph::Ascii(b) => draw_ascii_glyph(surf, x, y, b, color),
        }
    }

    /// Draw one 16x15 CJK glyph (PAL_DrawCharOnSurface, `font_width == 32`
    /// branch). Missing glyphs draw nothing.
    fn draw_cjk_glyph(&self, surf: &mut Surface, x: i32, y: i32, code: u16, color: u8) {
        let Some(&index) = self.big5_index.get(&code) else {
            return;
        };
        let offset = FON_GLYPH_BASE + index * FON_GLYPH_SIZE;
        let Some(glyph) = self.fon_data.get(offset..offset + FON_GLYPH_SIZE) else {
            return;
        };
        for row in 0..FONT_HEIGHT {
            let b0 = glyph[row as usize * 2];
            let b1 = glyph[row as usize * 2 + 1];
            for col in 0..8 {
                if b0 & (1 << (7 - col)) != 0 {
                    surf.put_pixel(x + col, y + row, color);
                }
            }
            for col in 0..8 {
                if b1 & (1 << (7 - col)) != 0 {
                    surf.put_pixel(x + 8 + col, y + row, color);
                }
            }
        }
    }
}

/// Draw one 8x15 ASCII glyph (PAL_DrawCharOnSurface, ISO-font branch).
fn draw_ascii_glyph(surf: &mut Surface, x: i32, y: i32, b: u8, color: u8) {
    let glyph = &ISO_FONT[b as usize];
    for row in 0..FONT_HEIGHT {
        let row_byte = glyph[row as usize];
        for col in 0..8i32 {
            if row_byte & (1 << col) != 0 {
                surf.put_pixel(x + col, y + row, color);
            }
        }
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    fn data() -> DataDir {
        std::env::set_var("PAL_DATA_DIR", concat!(env!("CARGO_MANIFEST_DIR"), "/pal"));
        DataDir::new().expect("game data dir")
    }

    #[test]
    fn loads_real_font_files() {
        let font = Font::load(&data()).expect("font load");
        // wor16.asc is 5088 bytes = 2544 2-byte codes.
        assert!(!font.big5_index.is_empty());
        assert!(font.big5_index.len() <= 2544);
        // wor16.fon glyph data must be present and large enough for every
        // code we indexed.
        let max_index = *font.big5_index.values().max().unwrap();
        assert!(font.fon_data.len() >= FON_GLYPH_BASE + (max_index + 1) * FON_GLYPH_SIZE);
    }

    #[test]
    fn wor16_asc_first_codes_are_big5() {
        // The task brief states wor16.asc starts with codes 0xB867, 0xC5E7;
        // verify directly against the raw file, independent of Font::load.
        let dir = data();
        let asc = dir.read_file("WOR16.ASC").unwrap();
        assert_eq!(asc.len(), 5088);
        let code0 = (asc[0] as u16) << 8 | asc[1] as u16;
        let code1 = (asc[2] as u16) << 8 | asc[3] as u16;
        assert_eq!(code0, 0xB867);
        assert_eq!(code1, 0xC5E7);
    }

    #[test]
    fn cjk_glyph_renders_nonzero_pixels_in_16x15_box() {
        let font = Font::load(&data()).expect("font load");
        let mut surf = Surface::new(64, 64);
        // 0xB867 is the first code in wor16.asc, guaranteed to be present.
        let text = [0xB8u8, 0x67];
        let end_x = font.draw_text(&mut surf, &text, 0, 0, 1, false);
        assert_eq!(end_x, 16);

        let mut any = false;
        for y in 0..surf.h {
            for x in 0..surf.w {
                if surf.get_pixel(x as i32, y as i32) != 0 {
                    any = true;
                    // must fall within the 16x15 glyph box
                    assert!(x < 16, "pixel outside glyph width at x={x}");
                    assert!(y < 15, "pixel outside glyph height at y={y}");
                }
            }
        }
        assert!(any, "expected at least one nonzero pixel for a real glyph");
    }

    #[test]
    fn missing_cjk_glyph_draws_nothing_but_advances() {
        let font = Font::load(&data()).expect("font load");
        let mut surf = Surface::new(32, 32);
        // Codes are drawn from a small (2544-entry) table; a Big5 lead byte
        // paired with 0x01 is not a valid trail byte and thus never present.
        let text = [0xB8u8, 0x01];
        let end_x = font.draw_text(&mut surf, &text, 0, 0, 1, false);
        assert_eq!(end_x, 16); // still advances by 16px
        assert!(surf.pixels.iter().all(|&p| p == 0));
    }

    #[test]
    fn ascii_letter_a_matches_hand_derived_bitmap() {
        // Row bytes for 'A' (65) transcribed directly from ascii.h:
        // 0x00,0x00,0x3e,0x63,0x63,0x63,0x7f,0x63,0x63,0x63,0x63,0x63,0x00,0x00,0x00
        // Bit j of each raw byte marks pixel column j (see ISO_FONT doc
        // comment for why no explicit bit-reversal is needed here).
        let expected_rows: [u8; 15] = [
            0x00, 0x00, 0x3e, 0x63, 0x63, 0x63, 0x7f, 0x63, 0x63, 0x63, 0x63, 0x63, 0x00, 0x00,
            0x00,
        ];
        let mut surf = Surface::new(8, 15);
        draw_ascii_glyph(&mut surf, 0, 0, b'A', 7);
        for (row, &byte) in expected_rows.iter().enumerate() {
            for col in 0..8i32 {
                let expected_on = byte & (1 << col) != 0;
                let got = surf.get_pixel(col, row as i32);
                assert_eq!(
                    got != 0,
                    expected_on,
                    "row {row} col {col}: expected {expected_on}, drew {got}"
                );
            }
        }
    }

    #[test]
    fn ascii_width_is_8_and_cjk_width_is_16() {
        let font = Font::load(&data()).expect("font load");
        assert_eq!(font.text_width(b"A"), 8);
        assert_eq!(font.text_width(&[0xB8, 0x67]), 16);
        assert_eq!(font.text_width(b"AB"), 16);
        assert_eq!(font.text_width(&[0xB8, 0x67, b'A']), 24);
    }

    #[test]
    fn draw_text_advances_by_text_width() {
        let font = Font::load(&data()).expect("font load");
        let mut surf = Surface::new(64, 64);
        let text = [0xB8u8, 0x67, b'A', b'B'];
        let end_x = font.draw_text(&mut surf, &text, 10, 10, 1, false);
        assert_eq!(end_x, 10 + font.text_width(&text));
    }

    #[test]
    fn shadow_draws_color_0_beneath_the_colored_glyph() {
        // Use a nonzero background so shadow pixels (color 0) are
        // distinguishable from an untouched surface (which also defaults to
        // 0) — otherwise "still 0" would prove nothing.
        let font = Font::load(&data()).expect("font load");
        let bg = 9u8;
        let mut plain = Surface::new(32, 32);
        let mut shadowed = Surface::new(32, 32);
        plain.clear(bg);
        shadowed.clear(bg);
        let text = [0xB8u8, 0x67];
        font.draw_text(&mut plain, &text, 4, 4, 5, false);
        font.draw_text(&mut shadowed, &text, 4, 4, 5, true);

        // Every glyph pixel set (color 5) in the plain render must also be
        // set to the same color at the same spot in the shadowed render
        // (the real glyph is always drawn last, on top of any shadow).
        for y in 0..plain.h {
            for x in 0..plain.w {
                let p = plain.get_pixel(x as i32, y as i32);
                if p == 5 {
                    assert_eq!(shadowed.get_pixel(x as i32, y as i32), 5);
                }
            }
        }
        // For a lit glyph pixel whose (+1,0) neighbor is still background
        // in the plain render, the shadowed render must have darkened that
        // neighbor to color 0 (PAL_DrawTextUnescape's shadow offsets).
        let mut found_shadow_pixel = false;
        for y in 0..plain.h {
            for x in 0..plain.w {
                if plain.get_pixel(x as i32, y as i32) == 5 {
                    let (sx, sy) = (x as i32 + 1, y as i32);
                    if plain.get_pixel(sx, sy) == bg && shadowed.get_pixel(sx, sy) == 0 {
                        found_shadow_pixel = true;
                    }
                }
            }
        }
        assert!(found_shadow_pixel, "expected a visible shadow pixel");
    }
}
